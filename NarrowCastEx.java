 class NarrowCastEx{
   public static void main(String[] ar){
       int marks=90,sub=40;
       float f=90.9;
       float fmarks=(float)f;
       double dmarks=(double)marks;
       short submarks=(short)sub;
System.out.println(marks);
System.out.println(dmarks);
System.out.println(submarks);
System.out.println(fmarks);
}
}